using System;
using System.Collections.Generic;
using System.Text;

namespace CalculatorWpfApp
{
    public class Algebra
    {
        public static double DOD(double num1, double num2) { return num1 + num2; }
        public static double VID(double num1, double num2) { return num1 - num2; }
        public static double MN(double num1, double num2) { return num1 * num2; }
        public static double DIL(double num1, double num2) { return num1 / num2; }
    }
}
